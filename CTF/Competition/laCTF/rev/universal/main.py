	for i in range(256):
		for j in range(256):
			for k in range(256):
				n = i ^ j * 7 ^ ~k + 13
				if n == enc:

					flag[pos[0]] = chr(i)
					flag[pos[1]] = chr(j)
					flag[pos[2]] = chr(k)


